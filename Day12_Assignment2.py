'''Q2. .Write a Python program to convert a list to a tuple. 

Input: listx = [5, 10, 7, 4, 15, 3] 

Output: (5, 10, 7, 4, 15, 3) '''


# Define the list
listx = [5, 10, 7, 4, 15, 3]

# Convert the list to a tuple using the tuple() function
tuplex = tuple(listx)

# Display the results
print("Original list:", listx)
print("Converted tuple:", tuplex)


'''
OUTPUT:
Original list: [5, 10, 7, 4, 15, 3]
Converted tuple: (5, 10, 7, 4, 15, 3)
'''

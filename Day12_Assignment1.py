'''Q1. Write a Python program to find the number of times 4 appears in the tuple. 

Input: tuplex = (2, 4, 5, 6, 2, 3, 4, 4, 7 )

 Output: 3 '''


# Define the tuple
tuplex = (2, 4, 5, 6, 2, 3, 4, 4, 7)

# Count occurrences of 4
count_of_4 = tuplex.count(4)

# Display the result
print("Given tuple:", tuplex)
print("Number of times 4 appears:", count_of_4)


'''
OUTPUT:
Given tuple: (2, 4, 5, 6, 2, 3, 4, 4, 7)
Number of times 4 appears: 3
'''
